    private void imprimeTableroGUI(Tablero t){
        Celda[][] tab = t.getTablero();
        Celda celda;
        int muroAbajo, muroArriba, muroIzquierda, muroDerecha;
        
        for(int i=0;i<t.getFilas();i++){
            for(int j=0;j<t.getCol();j++){
                celda = tab[i][j];
                muroAbajo = 1;
                muroArriba = 1;
                muroIzquierda = 1;
                muroDerecha = 1;
                if(celda.getValor() != 0){
                    tableroGUI[i][j].setText(Integer.toString(celda.getValor()));
                    tableroGUI[i][j].setBackground(new java.awt.Color(200, 200, 200));
                }
                if(celda.isMuroAbajo()){
                    muroAbajo = 4;
                }
                if(celda.isMuroArriba()){
                    muroArriba = 4;
                }
                if(celda.isMuroDerecha()){
                    muroDerecha = 4;
                }
                if(celda.isMuroIzquierda()){
                    muroIzquierda = 4;
                }
                tableroGUI[i][j].setBorder(javax.swing.BorderFactory.createMatteBorder(muroArriba, muroIzquierda, muroAbajo, muroDerecha, Color.DARK_GRAY));
            }
        }
    }

